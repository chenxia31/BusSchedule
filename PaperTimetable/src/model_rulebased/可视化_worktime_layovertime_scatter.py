    total_width, n = 0.75, 3
    width = total_width / n

    x1 = list(range(1, 1 + len(w1)))
    plt.bar(x1, w1, label='origin', width=width)

    x2 = list(range(1, 1 + len(w2)))
    for i in range(len(x2)):
        x2[i] = x2[i] + width
    plt.bar(x2, w2, label='origin+target1', width=width)

    x3 = list(range(1, 1 + len(w3)))
    for i in range(len(x3)):
        x3[i] = x3[i] + 2 * width
    plt.bar(x3, w3, width=width, label='origin+target1(init)')
