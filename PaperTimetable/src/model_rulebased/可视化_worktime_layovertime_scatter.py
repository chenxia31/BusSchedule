def plot_rec(data1, data2):
    '''
    绘制两个数据之间工作时间的对比
    条形图，是有对比的
    '''
    x1 = list(range(len(data1)))
    x2 = list(range(len(data2)))
    total_width, n = 0.8, 2
    width = total_width / n

    plt.bar(x1, data1, width=width, label='origin')

    for i in range(len(x2)):
        x2[i] = x2[i] + width

    plt.bar(x2, data2, width=width, label='origin+target0')
    plt.xlabel('index')
    plt.ylabel('work_time(min)')
    plt.legend()
    plt.show()


